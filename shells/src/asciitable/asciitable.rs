enum AsciiTable {
    NUL = 0x00,
    //
    SOT = 0x02,
    // start of text
    BEL = 0x07,
    // bell            (\a)
    BS = 0x08,
    // backspace       (\b)
    TAB = 0x09,
    // tab             (\t)
    LF = 0x0a,
    // line feed       (\n)
    VT = 0x0b,
    // vertical tab    (\v)
    FF = 0x0c,
    // form feed
    CR = 0x0d,
    // carriage return (\r)
    ESC = 0x1b, // escape
}